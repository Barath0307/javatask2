package com.task2;

public class EmployeePerson {
	    private String name;
	    private int age;
	    public EmployeePerson(String name, int age) {
	        this.name = name;
	        this.age = age;
	    }
	    public String getName() {
	        return name;
	    }
	    public int getAge() {
	        return age;
	    }
	}
